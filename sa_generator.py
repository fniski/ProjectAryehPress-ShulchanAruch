@dataclass
class Commentary:
    """A single commentary text block."""
    name_he: str
    name_en: str
    # List of seif-katan dicts: {"he": str, "en": str}
    seifim_ketanim: list = field(default_factory=list)


@dataclass
class SeifData:
    """A single seif of the Mechaber."""
    mechaber_he: str
    mechaber_en: str  # translation (or lorem)
    rema_he: str = ""
    rema_en: str = ""


@dataclass
class SimanData:
    """All texts for a single siman of SA."""
    chelek_he: str           # e.g. "אורח חיים"
    chelek_en: str           # e.g. "Orach Chaim"
    siman_num_he: str        # e.g. 'ר"צ'
    siman_num_en: str        # e.g. "290"
    topic_he: str            # e.g. "הלכות שבת"
    topic_en: str            # e.g. "Laws of Shabbos"
    daf_he: str              # e.g. 'קנ"ט'

    seifim: list[SeifData] = field(default_factory=list)

    # Page 1: the 2 main commentaries flanking the mechaber
    comm_left: Commentary = None   # e.g. Magen Avraham
    comm_right: Commentary = None  # e.g. Taz / Magen David


