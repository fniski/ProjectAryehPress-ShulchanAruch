"""
Shulchan Aruch Two-Page Layout Generator

Given Hebrew texts for a siman and its commentaries, generates a 2-page
LuaLaTeX document:
  Page 1: Mechaber + Rema (center) with 2 main commentaries flanking,
          all in interlinear Hebrew+Translation format.
  Page 2: (TODO) Secondary commentaries + side panels.

Usage:
    uv run sa_generator.py
"""

